public class Calc {
    public static void main(String[] args) {
        int a = 30;
        int b = 10;
        int add = a + b;
        int sub = a - b;
        int mul = a * b;
        int div = a / b;
        int rem = a % b;
        System.out.println("After Addition: "+add);
        System.out.println("After Subtraction: "+sub);
        System.out.println("After Multiplication: "+mul);
        System.out.println("After Dividation: "+div);
        System.out.println("Remainder: "+rem);
    }
}
